package commands;

/**
 * Команда 'help'. Выводит справку по всем доступным командам.
 * @author kifmu
 */
public class Help implements Command {
    public Help() {}
    /**
     * Выполняет команду вывода справки по доступным командам.
     * @param arguments аргументы команды
     * @return многострочная строка с описанием всех доступных команд
     */
    @Override
    public String execute(String[] arguments) {
        return "Справка по доступным командам:\n" +
                "info : вывести информацию о коллекции (тип, дата инициализации, количество элементов)\n" +
                "show : вывести все элементы коллекции в строковом представлении\n" +
                "insert {element} : добавить новый элемент с автоматически сгенерированным ключом\n" +
                "update id {element} : обновить значение элемента коллекции, id которого равен заданному\n" +
                "remove_key {key} : удалить элемент из коллекции по его ключу\n" +
                "clear : очистить коллекцию\n" +
                "save : сохранить коллекцию в файл\n" +
                "execute_script file_name : считать и исполнить скрипт из указанного файла\n" +
                "exit : завершить программу (без сохранения в файл)\n" +
                "remove_greater {element} : удалить из коллекции все элементы, превышающие заданный\n" +
                "history : вывести последние 9 команд (без их аргументов)\n" +
                "replace_if_greater {key} {element} : заменить значение по ключу, если новое значение больше старого\n" +
                "print_unique_eye_color : вывести уникальные значения поля eyeColor всех элементов\n" +
                "print_field_descending_height : вывести значения поля height всех элементов в порядке убывания\n" +
                "help : вывести эту справку";
    }
    /**
     * Возвращает описание команды.
     * @return строка с описанием команды
     */
    @Override
    public String getDescription() {
        return "help : вывести справку по доступным командам";
    }
    /**
     * Возвращает название команды.
     * @return название команды
     */
    @Override
    public String getName() {
        return "help";
    }
}